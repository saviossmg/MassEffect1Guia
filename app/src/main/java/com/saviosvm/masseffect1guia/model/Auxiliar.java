public class Auxiliar {

    private int id;
    private int ponto;

    public Auxiliar() {
        this.id = id;
        this.ponto = ponto;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getPonto() {
        return ponto;
    }

    public void setPonto(int ponto) {
        this.ponto = ponto;
    }

}
